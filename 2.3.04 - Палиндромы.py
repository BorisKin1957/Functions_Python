'''Задача 4: Палиндромы
Условие:
На вход программе подается строка со словами в нижнем регистре, разделенными пробелами.

Используя filter(), отберите из них только те слова, которые являются палиндромами
(читаются одинаково слева направо и справа налево). Результат выведите в виде списка.

Формат входных данных:
Одна строка со словами через пробел.

Формат выходных данных:
Список (list) слов-палиндромов.'''

# Считываем строку и разбиваем её на список слов по пробелам
# Функция filter() проверяет каждое слово с помощью лямбда-функции:
#  word[::-1] — переворачиваем слово
# Если слово читается одинаково в обоих направлениях — оно проходит фильтр
# Результат преобразуем в список и выводим
print(list(filter(lambda word: word == word[::-1], input().split())))