'''Задача 3: Удаление запятых
Условие:
На вход программе подается строка, содержащая элементы, разделенные запятыми.
Некоторые элементы могут быть пустыми.

Ваша задача — отфильтровать список, полученный после разделения строки,
удалив из него все пустые строки. Результат выведите в виде списка.

Формат входных данных:
Одна строка с элементами, разделенными запятыми.

Формат выходных данных:
Список (list) без пустых строк.

Sample Input:

apple,,banana,cherry,
Sample Output:

['apple', 'banana', 'cherry']'''

# Получаем строку от пользователя
# Разделяем её по запятым с помощью split(',')
# Получаем список, который может содержать пустые строки (например, из-за нескольких запятых подряд)
# Фильтруем список: оставляем только те элементы, которые не являются пустыми (bool(word) == True)
# Преобразуем отфильтрованный итератор в список и выводим его

print(list(filter(lambda word: bool(word), input().split(','))))


'''Пояснение:

input().split(',') — разбивает введённую строку по запятым. 
Например, 'apple,,banana,cherry,' превратится в ['apple', '', 'banana', 'cherry', ''].
filter(lambda word: bool(word), ...) — фильтрует элементы, оставляя только те, 
которые при приведении к bool дают True. Пустая строка '' даёт False, 
поэтому она удаляется.
list(...) — преобразует результат фильтрации обратно в список.
print(...) — выводит итоговый список.
✅ Этот код корректно решает задачу и проходит тест на примере.'''