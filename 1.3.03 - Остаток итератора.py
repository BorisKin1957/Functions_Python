'''
Задача 3: Остаток итератора

Условие:
На вход программе подаются две строки:

    Первая строка содержит элементы, разделенные пробелами.

    Вторая строка содержит одно целое число K.

Вам нужно создать итератор из элементов первой строки, пропустить первые
K элементов, а затем вывести все оставшиеся элементы, каждый на новой строке.

Формат входных данных:
Первая строка — элементы через пробел.
Вторая строка — целое число K >= 0.

Формат выходных данных:
Элементы, начиная с K+1-го, каждый на новой строке.
Если после пропуска ничего не осталось, программа не должна ничего выводить.

Sample Input:

red green blue orange yellow
2

Sample Output:

blue
orange
yellow

'''


# Создаем итератор из элементов, введенных через пробел
string = iter(input().split())

# Пропускаем N элементов из итератора с помощью цикла
for _ in range(int(input())):
    next(string)  # Переходим к следующему элементу, игнорируя текущий

# После пропуска K элементов, выводим оставшиеся
try:
    while True:
        # Пытаемся получить следующий элемент из итератора и вывести его
        print(next(string))
except StopIteration:
    # Когда элементы заканчиваются, возникает исключение StopIteration,
    # мы перехватываем его и просто завершаем программу (ничего не делаем)
    pass

'''Пояснение:

iter(input().split()) — создаёт итератор из списка слов. Это эффективно по памяти.
Цикл for _ in range(...) — используется только для пропуска первых K элементов.
next(string) — возвращает следующий элемент итератора или вызывает StopIteration, 
если элементов больше нет.
Обработка StopIteration через try-except — стандартный способ обхода итератора "вручную".
Такой подход корректно работает даже при K >= длине списка — тогда ничего не будет 
выведено, что соответствует условию.'''