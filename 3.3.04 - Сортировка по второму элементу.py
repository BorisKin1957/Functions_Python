'''Задача 4: Сортировка по второму элементу
Условие:
На вход программе сначала подается число N — количество записей. Затем в N
следующих строках вводятся пары: <Название> <Число>.

Вам нужно отсортировать эти записи по числу (второму элементу) по возрастанию.
Выведите отсортированные пары, каждую на новой строке, в исходном формате.

Формат входных данных:
В первой строке — число N.
В следующих N строках — <Название> <Число> через пробел.

Формат выходных данных:
N строк с отсортированными парами.

Sample Input:

4
apple 50
banana 30
cherry 45
date 30
Sample Output:

banana 30
date 30
cherry 45
apple 50'''

# Считываем количество записей
n = int(input())

# Генератор: для каждой из n строк считываем строку, разбиваем её по пробелам и создаём список [название, число]
sales_data = (input().split() for _ in range(n))

# Сортируем данные:
# key=lambda x: [int(x[1]), x[0]] означает, что сначала сортируем по числу (второй элемент, индекс 1), преобразованному в int,
# а при равных числах — по названию (первый элемент, индекс 0) в лексикографическом порядке
sorted_sales_data = sorted(sales_data, key=lambda x: [int(x[1]), x[0]])

# Выводим отсортированные пары: распаковываем каждый список (например, ['banana', '30']) и печатаем через пробел
for product in sorted_sales_data:
    print(*product)
