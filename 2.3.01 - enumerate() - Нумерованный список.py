'''Задача 1: Нумерованный список
Условие:
На вход программе подается строка, содержащая названия дел, разделенные запятой.

Используя enumerate() с параметром start, выведите эти дела в виде
нумерованного списка, где нумерация начинается с 1.
Каждое дело должно выводиться на новой строке в формате "N. <название дела>".

Формат входных данных:
Одна строка с делами, разделенными запятой.

Формат выходных данных:
Нумерованный список дел, каждое на новой строке.

Sample Input:

Помыть посуду,Выучить Python,Сделать зарядку
Sample Output:

1. Помыть посуду
2. Выучить Python
3. Сделать зарядку'''

# Считываем строку с делами и разбиваем её по запятым, получая список
cases = input().split(',')

# Проходим по списку дел, используя enumerate, чтобы получить номер (начиная с 1) и само дело
for count, case in enumerate(cases, start=1):
    # Выводим дело в формате "N. <название>"
    print(f'{count}. {case}')