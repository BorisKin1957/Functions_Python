'''Задача 5: Общая выручка

Условие:
На вход программе подается число  N — количество записей о продажах.
В следующих N строках вводятся данные по каждой записи в формате: <Цена> <Количество>.

Используя функцию sum() и генераторное выражение, посчитайте и выведите общую
выручку (сумму произведений цены на количество для каждой записи).

Формат входных данных:
В первой строке — число  N.
В следующих N строках — цена и количество (целые числа) через пробел.

Формат выходных данных:
Одно целое число — общая выручка.

Sample Input:

3
100 2
50 5
20 10
Sample Output:

650'''

# количество записей
n = int(input())

# генератор, который будет считывать n строк, разбивать каждую на два числа (цена, количество)
# map(int, input().split()) применяется к каждой из n строк
sales_data = (map(int, input().split()) for _ in range(n))

# Используем генераторное выражение: для каждой пары (цена x, количество y) вычисляем x * y
# sum() суммирует все произведения — получаем общую выручку
total_revenue = sum(x * y for x, y in sales_data)

# Выводим итоговую сумму
print(total_revenue)
