'''Задача 3: Элементы на четных позициях
Условие:
На вход программе подается строка, содержащая элементы, разделенные пробелом.

Используя enumerate(), выведите только те элементы, которые стоят на четных
индексах (0, 2, 4, ...). Каждый такой элемент должен выводиться на новой строке.

Формат входных данных:
Одна строка с элементами, разделенными пробелами.

Формат выходных данных:
Элементы с четных позиций, каждый на новой строке.

Sample Input:

A B C D E F G
Sample Output:

A
C
E
G'''

'''Комментарий к строке:

1. input().split() — считывает строку с клавиатуры и разбивает её на список элементов по пробелам.
2. enumerate(...) — создаёт объект, который возвращает пары (индекс, значение) для каждого элемента списка.
3. (item[1] for item in ... if item[0] % 2 == 0) — генератор, перебирающий пары (индекс, значение) и выбирающий только те значения (item[1]), чей индекс (item[0]) является чётным.
4. *() — распаковывает полученные значения из генератора в аргументы функции print.
5. sep='\n' — указывает, что каждый элемент должен выводиться с новой строки.

Итог: строка корректно решает задачу — выводит элементы, стоящие на чётных позициях (0, 2, 4, ...), каждый с новой строки.'''

print(*(item[1] for item in enumerate(input().split()) if item[0] %2 == 0), sep='\n')