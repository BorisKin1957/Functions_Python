'''
Задача 5: Сумма квадратов четных чисел

Условие:
На вход программе подается одна строка, содержащая целые числа,
записанные через пробел.

Ваша задача — написать программу, которая вычисляет сумму квадратов только
четных чисел из этой последовательности.

Важно: Решение должно быть максимально эффективным и использовать генераторное
выражение, результат которого напрямую передается в функцию sum().
Не создавайте промежуточных списков.

Формат входных данных:
Одна строка с целыми числами, разделенными пробелами.

Формат выходных данных:
Одно целое число — итоговая сумма.'''


# Создаем генератор, преобразуя каждое число из строки в int
# Генератор не загружает все числа в память сразу — экономит ресурсы
nums = (int(i) for i in input().split())

# Вычисляем сумму квадратов четных чисел:
#   - перебираем числа из генератора nums
#   - проверяем, что число чётное: not num % 2 → True, если остаток от деления на 2 равен 0
#   - возводим чётное число в квадрат и включаем в сумму
print(sum(num ** 2 for num in nums if not num % 2))