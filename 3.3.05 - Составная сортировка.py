'''Задача 5: Составная сортировка
Условие:
На вход программе сначала подается число N — количество студентов.
Затем в N следующих строках вводятся данные: <Имя> <Балл>.

Вам нужно отсортировать студентов по следующим правилам:

В первую очередь — по убыванию баллов.

Если баллы одинаковы — по имени в алфавитном порядке (по возрастанию).

Выведите отсортированный список, каждую запись на новой строке.

Формат входных данных:
В первой строке — число N.
В следующих N строках — <Имя> <Балл> через пробел.

Формат выходных данных:
N строк с отсортированными данными.

Sample Input:

5
Anna 92
Boris 81
Victor 75
Galina 81
Dmitry 92
Sample Output:

Anna 92
Dmitry 92
Boris 81
Galina 81
Victor 75'''

# Читаем количество студентов
n = int(input())

# Считываем N строк, каждая содержит имя и балл, разделяем по пробелу
# Результат — список списков вида [['Anna', '92'], ['Boris', '81'], ...]
total_results = [input().split() for _ in range(n)]

# Сортируем список по двум критериям:
# 1. По убыванию баллов: для этого используем -int(x[1]) (минус для обратной сортировки)
# 2. При равных баллах — по имени в алфавитном порядке (по возрастанию): x[0]
sorted_results = sorted(total_results, key=lambda x: [-int(x[1]), x[0]])

# Выводим отсортированные данные: имя и балл через пробел
for name, score in sorted_results:
    print(name, score)

