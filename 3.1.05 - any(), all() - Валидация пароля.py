'''Задача 5: Сложная валидация пароля
Условие:
На вход программе подается одна строка — пароль для проверки.
Пароль считается надежным, если выполняются все три условия одновременно:

Его длина не менее 8 символов.
В нем есть хотя бы одна цифра.
В нем есть хотя бы одна заглавная буква.

Напишите программу, которая выводит True, если пароль надежный,
и False в противном случае.

Формат входных данных:
Одна строка без пробелов.

Формат выходных данных:
True или False.

Sample Input:

Stepik123
Sample Output:

True'''


# Определение функции password_test, которая принимает строку password и возвращает булево значение
def password_test(password: str) -> bool:
    # Проверка первого условия: длина пароля не менее 8 символов (т.е. >= 8)
    test_1 = len(password) > 7  # len(password) возвращает количество символов; > 7 эквивалентно >= 8

    # Проверка второго условия: содержит хотя бы одну цифру
    # Генераторное выражение перебирает каждый символ sym в пароле и проверяет, является ли он цифрой (isdigit())
    # Функция any() возвращает True, если хотя бы один элемент генератора — True
    test_2 = any(sym.isdigit() for sym in password)

    # Проверка третьего условия: содержит хотя бы одну заглавную букву
    # Генераторное выражение проверяет, является ли каждый символ заглавной буквой (isupper())
    # any() возвращает True, если хотя бы один символ — заглавный
    test_3 = any(sym.isupper() for sym in password)

    # all() принимает список булевых значений [test_1, test_2, test_3]
    # и возвращает True только если все элементы списка — True
    # То есть, пароль надежный, если выполнены все три условия
    return all([test_1, test_2, test_3])


# Считываем строку с клавиатуры (ввод пользователя) и передаем её в функцию password_test
# Результат (True/False) выводим на экран
print(password_test(input()))