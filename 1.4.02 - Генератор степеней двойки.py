'''Задача 2: Генератор степеней двойки

Условие:
Напишите функцию-генератор powers_of_two(n), которая принимает на вход
целое число n (n >= 0) и генерирует последовательность из n + 1 степеней двойки,
от 2⁰ до 2ⁿ.'''

def powers_of_two(n: int) -> int:
    # Перебираем числа от 0 до n включительно (всего n + 1 элементов)
    for i in range(0, n + 1):
        # Вычисляем 2 в степени i и "выдаём" значение через yield
        yield 2 ** i

# Этот код не нужно отправлять в решение, он для проверки
# Преобразуем результат генератора в список и выводим его
print(list(powers_of_two(4)))  # Ожидаемый вывод: [1, 2, 4, 8, 16]