'''
Задача 3: Первая буква

Условие:
На вход программе подается строка, содержащая слова в разном регистре.
Ваша задача — получить последовательность, состоящую только из первых
букв каждого слова, приведенных к верхнему регистру.

Используйте map() и lambda-функцию. Результат выведите в виде списка.

Формат входных данных:
Одна строка со словами, разделенными пробелами.

Формат выходных данных:
Список (list) с первыми буквами в верхнем регистре.

Sample Input:

Python function map

Sample Output:

['P', 'F', 'M']

'''

# Разбиваем строку на слова, для каждого слова берём первую букву, переводим в верхний регистр и возвращаем список
print(list(map(lambda word: next(iter(word)).upper(), input().split())))

'''Пояснение:

input().split() — разбивает ввод на список слов по пробелам.
map() применяет лямбду к каждому слову: lambda word: next(iter(word)) — 
получает первый символ слова (работает даже с пустыми строками безопасно, 
хотя в данной задаче это не критично). .upper() — преобразует первую букву 
в верхний регистр.
Результат — список заглавных первых букв каждого слова.'''