'''Задача 4: Лучший студент
Условие:
На вход программе сначала подается число N — количество студентов. Затем в N
следующих строках вводятся данные о студентах в формате <Имя> <Балл>.

Используя функцию max() с аргументом key, определите студента с наивысшим баллом
и выведите на экран его имя.

Формат входных данных:
В первой строке — число N.
В следующих N строках — имя и балл через пробел.

Формат выходных данных:
Имя студента с максимальным баллом.

Sample Input:

3
Anna 92
Boris 58
Victor 75
Sample Output:

Anna'''

# Создаём пустой словарь для хранения имён и баллов студентов
results = {}

# Читаем количество студентов
for _ in range(int(input())):
    # Разделяем введённую строку на имя и балл
    name, score = input().split()
    # Преобразуем балл в целое число и сохраняем в словаре
    results[name] = int(score)

# Находим пару (имя, балл) с максимальным баллом, используя max() и lambda-функцию по значению (баллу)
# Берём только имя ([0]) из этой пары
best_result = max(results.items(), key=lambda x: x[1])[0]

# Выводим имя лучшего студента
print(best_result)



