'''Задача 2: Создание словаря
Условие:
На вход программе подаются две строки:

Первая строка содержит ключи, разделенные пробелом.

Вторая строка содержит значения, разделенные пробелом.

Используя zip(), объедините эти два списка в пары, а затем преобразуйте результат в словарь (dict). Выведите полученный словарь на экран.

Формат входных данных:
Две строки, элементы в которых разделены пробелами.

Формат выходных данных:
Словарь, созданный из ключей и значений.

Sample Input:

name age city
Anna 25 Moscow
Sample Output:

{'name': 'Anna', 'age': '25', 'city': 'Moscow'}'''


# Создание словаря из двух строк: одна с ключами, другая со значениями
# С помощью zip() объединяем пары (ключ, значение), затем преобразуем в словарь
# input().split() разбивает строку на список по пробелам
print({key: value for key, value in zip(input().split(), input().split())})


'''Пояснение:

input().split() — читает строку и разбивает её на список слов по пробелам.
zip(keys, values) — объединяет элементы двух списков в пары.
Генератор словаря {key: value for key, value in zip(...)} создаёт словарь из этих пар.
Результат выводится одной строкой кода.'''



