'''
Задача 4: Итерация вручную

Условие:
На вход программе подается одна строка с элементами через пробел. 
Ваша задача — вывести все элементы этой последовательности, каждый на новой строке.

Ограничение: В вашем решении запрещено использовать цикл for. 
Вы должны симулировать его работу вручную.

Формат входных данных:
Одна строка с элементами, разделенными пробелами.

Формат выходных данных:
Все элементы из входной строки, каждый на новой строке.

Sample Input:
Stepik is a great platform

Sample Output:
Stepik
is
a
great
platform
'''

# Считываем строку, разбиваем её по пробелам и получаем список слов
# Затем создаём итератор из этого списка с помощью iter()
string = iter(input().split())

try:
    # Входим в бесконечный цикл, который будет работать до тех пор,
    # пока не закончатся элементы в итераторе
    while True:
        # Получаем следующий элемент из итератора с помощью next()
        # Если элемент есть — выводим его
        print(next(string))
except StopIteration:
    # Когда элементы заканчиваются, возникает исключение StopIteration
    # Мы перехватываем его и просто завершаем программу (ничего не делаем)
    pass


