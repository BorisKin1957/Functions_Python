'''
Задача 5: Общая стоимость

Условие:
На вход программе подаются две строки:

    Первая строка содержит цены товаров (целые числа через пробел).

    Вторая строка содержит количество каждого товара (целые числа через пробел).

Используя map() с lambda-функцией, которая принимает два аргумента,
посчитайте общую стоимость для каждой позиции (цена * количество).
Результат выведите в виде списка.

Формат входных данных:
Две строки, каждая содержит целые числа через пробел.

Формат выходных данных:
Список (list) с итоговыми стоимостями.

Sample Input:

100 20 50
3 5 2

Sample Output:

[300, 100, 100]

'''

# Считываем первую строку и преобразуем её элементы в целые числа, создавая итератор цен
prices = map(int, input().split())

# Считываем вторую строку и преобразуем её элементы в целые числа, создавая итератор количеств
counts = map(int, input().split())

# Используем map с lambda-функцией, чтобы умножить каждую цену на соответствующее количество
# lambda x, y: x * y — функция, принимающая два аргумента и возвращающая их произведение
# map применяет эту функцию к парам элементов из prices и counts
# Результат преобразуется в список и выводится
print(list(map(lambda x, y: x * y, prices, counts)))